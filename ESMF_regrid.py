def create_coordax_regridder(
    weight_file: str,
    src_dims: Tuple[str, ...],
    dst_dims: Tuple[str, ...],
    src_coords: Dict[str, jnp.ndarray],
    dst_coords: Dict[str, jnp.ndarray]
):
    """
    Create a regridder that preserves coordax metadata.
    
    This bridges ESMFRegridder with the coordax-based coupler design.
    """
    import coordax as cx
    
    # Infer shapes from coordinates
    src_shape = tuple(len(src_coords[dim]) for dim in src_dims)
    dst_shape = tuple(len(dst_coords[dim]) for dim in dst_dims)
    
    # Create base regridder
    base_regridder = ESMFRegridder(weight_file, src_shape, dst_shape)
    
    def regrid_with_coords(var: cx.Variable) -> cx.Variable:
        """Regrid coordax variable while updating coordinates."""
        # Apply regridding to data
        new_data = base_regridder(var.data)
        
        # Create new variable with destination coordinates
        return cx.Variable(
            data=new_data,
            dims=dst_dims,
            coords=dst_coords,
            attrs=var.attrs
        )
    
    return regrid_with_coords
