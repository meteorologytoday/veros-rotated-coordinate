def create_regridder_pair(
    src_to_dst_file: str,
    dst_to_src_file: str,
    src_shape: Tuple[int, ...],
    dst_shape: Tuple[int, ...]
) -> Tuple[ESMFRegridder, ESMFRegridder]:
    """
    Create a pair of regridders for bidirectional interpolation.
    
    Parameters
    ----------
    src_to_dst_file : str
        Weight file for source to destination
    dst_to_src_file : str
        Weight file for destination to source
    src_shape : tuple
        Source grid shape
    dst_shape : tuple
        Destination grid shape
        
    Returns
    -------
    forward_regridder, backward_regridder
    """
    forward = ESMFRegridder(src_to_dst_file, src_shape, dst_shape)
    backward = ESMFRegridder(dst_to_src_file, dst_shape, src_shape)
    
    return forward, backward


def create_regridder_from_xarray(
    weight_file: str,
    src_da: xr.DataArray,
    dst_da: xr.DataArray
) -> ESMFRegridder:
    """
    Create regridder inferring shapes from xarray DataArrays.
    
    Parameters
    ----------
    weight_file : str
        Path to ESMF weight file
    src_da : xr.DataArray
        Example source data array
    dst_da : xr.DataArray
        Example destination data array
        
    Returns
    -------
    ESMFRegridder
    """
    src_shape = src_da.shape
    dst_shape = dst_da.shape
    
    return ESMFRegridder(weight_file, src_shape, dst_shape)


def verify_conservation(
    regridder: ESMFRegridder,
    src_data: jnp.ndarray,
    src_areas: Optional[jnp.ndarray] = None,
    dst_areas: Optional[jnp.ndarray] = None
) -> Dict[str, float]:
    """
    Verify conservation properties of regridding.
    
    Parameters
    ----------
    regridder : ESMFRegridder
        The regridder to test
    src_data : jnp.ndarray
        Test data on source grid
    src_areas : jnp.ndarray, optional
        Cell areas on source grid
    dst_areas : jnp.ndarray, optional
        Cell areas on destination grid
        
    Returns
    -------
    dict
        Conservation diagnostics
    """
    dst_data = regridder(src_data)
    
    # Calculate integrals
    if src_areas is not None:
        src_integral = jnp.sum(src_data * src_areas)
    else:
        src_integral = jnp.sum(src_data)
    
    if dst_areas is not None:
        dst_integral = jnp.sum(dst_data * dst_areas)
    else:
        dst_integral = jnp.sum(dst_data)
    
    # Calculate errors
    abs_error = dst_integral - src_integral
    if jnp.abs(src_integral) > 1e-10:
        rel_error = abs_error / src_integral
    else:
        rel_error = abs_error
    
    return {
        'src_integral': float(src_integral),
        'dst_integral': float(dst_integral),
        'absolute_error': float(abs_error),
        'relative_error': float(rel_error),
        'conserves': float(jnp.abs(rel_error)) < 1e-6
    }


